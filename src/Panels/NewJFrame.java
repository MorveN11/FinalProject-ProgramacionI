    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
       
        String search = "SELECT *FROM prueba";
        
        try {
                
                Connection cn = DataBaseData.connect();
                PreparedStatement pst = (PreparedStatement) cn.prepareStatement(search);
                ResultSet rs = pst.executeQuery();
                
                if(rs.next()){
                    //Datos consultados
                    //leer Binario
                    Blob blob = rs.getBlob("Image");
                    //pasar el binario a imagen
                    byte[] data = blob.getBytes(1, (int) blob.length());
                    //lee la imagen
                    BufferedImage img = null;
                    try {
                        img = ImageIO.read(new ByteArrayInputStream(data));
                    } catch (IOException e) {
                        Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, e);
                    }
                    
                    ImageIcon icono = new ImageIcon(img);
                    Icon imagen = new ImageIcon(icono.getImage().getScaledInstance(pruebaimagen.getWidth(), pruebaimagen.getHeight(), Image.SCALE_DEFAULT));
                    pruebaimagen.setIcon(imagen);
                }
                
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Â¡Error al cargar!");
                System.out.println("Error al cargar foto: " + e);
            }
           
    }//GEN-LAST:event_btnConsultarActionPerformed

